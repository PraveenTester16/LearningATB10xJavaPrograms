package Math_Calculations;

public class Post_Inc_Ex3 {

    public static void main(String[] args) {

        	int a = 20;
	System.out.println(a); // prints 20
	int b = a++; // post inc so b is 20
	System.out.println(b); // prints 20
	System.out.println(a); // prints incremented value of 21


    }
}
